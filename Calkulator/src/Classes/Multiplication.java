package Classes;

public class Multiplication {

    public double multipli (double liczba1, double liczba2){

        double wynikMulti = liczba1 * liczba2;
        return  wynikMulti;
    }

    public double multipli (double liczba1, double liczba2, double liczba3){

        double wynikMulti = liczba1 * liczba2 * liczba3;
        return  wynikMulti;
    }

    public double multipli (double liczba1, double liczba2, double liczba3, double liczba4){

        double wynikMulti = liczba1 * liczba2 * liczba3 * liczba4;
        return  wynikMulti;
    }
}
