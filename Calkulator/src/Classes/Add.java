package Classes;

public class Add {

    public double dodawanie (double liczba1, double liczba2){

        double wynikDodawania = liczba1 + liczba2;
        return wynikDodawania;

    }

    public double dodawanie (double liczba1, double liczba2, double liczba3){

        double wynikDodawania = liczba1 + liczba2 + liczba3;
        return wynikDodawania;

    }

    public double dodawanie (double liczba1, double liczba2, double liczba3, double liczba4){

        double wynikDodawania = liczba1 + liczba2 + liczba3 + liczba4;
        return wynikDodawania;

    }



}
